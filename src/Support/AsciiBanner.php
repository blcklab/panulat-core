    private const HEIGHT = 5;

    /**
     * @return list<string>
     */
    public static function render(string $text, int $spacing = 1): array
    {
        $spacing = max(0, $spacing);
        $gap = str_repeat(' ', $spacing);
        $lines = array_fill(0, self::HEIGHT, '');
        $characters = str_split(strtoupper($text));

        foreach ($characters as $character) {
            $glyph = self::glyph($character);

            foreach ($glyph as $index => $segment) {
                $lines[$index] .= ($lines[$index] === '' ? '' : $gap) . $segment;
            }
        }

        $trimmed = [];

        foreach ($lines as $line) {
            $trimmed[] = rtrim($line);
        }

        return $trimmed;
    }

    public static function text(string $text, int $spacing = 1): string
    {
        return implode(PHP_EOL, self::render($text, $spacing));
    }

    /**
     * @return array<int, string>
     */
    private static function glyph(string $character): array
    {
        $font = self::font();

        return $font[$character] ?? $font['?'];
    }

